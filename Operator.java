package basics;

public class Operator {
    public static void main(String[] args) {
        //arithmatic operator
        int num1 = 24;
        int num2 = 5;
        int res = num1+ num2;
        System.out.println(res);
        //multiplication ops
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2); //remainder
        System.out.println(++num1);
        System.out.println(--num1);

//        relational ops
        int a  = 45;
        int b = 45;
        System.out.println(a=b);
        System.out.println(a < b);

        //logical operator


    }
}
